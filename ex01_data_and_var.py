# coding=utf-8

# 变量赋值
r = 18
pi = 3.14159

# 算数运算
c = 2 * pi * r
s = pi * r ** 2
print(c)
print(s)

# 字符串连接
s1 = 'This is '
s2 = 'a string'
s = s1 + s2
print(s)

# 关系运算
x = 100
r1 = (x <= 100)
r2 = (x + 3) / 8 != 6
print(r1)
print(r2)

# 布尔运算
con1 = True and False
con2 = not False or False
print(con1 and con2)


